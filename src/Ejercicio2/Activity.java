package Ejercicio2;

public class Activity {
   private String name; 
   private double cost; 
   private boolean ofSocialConcern;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getCost() {
		return cost;
	}
	public void setCost(double cost) {
		this.cost = cost;
	}
	public boolean isOfSocialConcern() {
		return ofSocialConcern;
	}
	public void setOfSocialConcern(boolean ofSocialConcern) {
		this.ofSocialConcern = ofSocialConcern;
	}
   

}
